/*Crie uma variável chamada temperatura e atribua o valor 30. 
Se a temperatura for maior que 25, exiba a mensagem "Está calor!".
Caso contrário, exiba "Está fresco!".
 */

let temperatura = 30;

if (temperatura > 25) {
  console.log("Está calor!");
}
else {
  console.log("Está fresco!");
}

