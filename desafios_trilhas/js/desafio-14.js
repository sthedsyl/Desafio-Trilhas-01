// Função que calcula a soma de dois números
function somar(a, b){
  // Retorna a soma de a e b
  return a + b;
}

// Declaração de duas variáveis
let num1 = 10;
let num2 = 20;

// Imprime a soma dos dois números
console.log(`A soma de ${num1} e ${num2} é ${somar(num1, num2)}`);